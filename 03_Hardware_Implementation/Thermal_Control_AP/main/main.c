void app_main(void)
{
    //Initialize NVS
    esp_err_t ret = nvs_flash_init();
    if (ret == ESP_ERR_NVS_NO_FREE_PAGES || ret == ESP_ERR_NVS_NEW_VERSION_FOUND) {
      ESP_ERROR_CHECK(nvs_flash_erase());
      ret = nvs_flash_init();
    }
    ESP_ERROR_CHECK(ret);

    ESP_LOGI(TAG, "ESP_WIFI_MODE_AP");
    wifi_init_softap();

        ESP_LOGI(TAG, "[APP] Startup..");
    ESP_LOGI(TAG, "[APP] Free memory: %" PRIu32 " bytes", esp_get_free_heap_size());
    ESP_LOGI(TAG, "[APP] IDF version: %s", esp_get_idf_version());
    esp_log_level_set("*", ESP_LOG_INFO);
    esp_log_level_set("mqtt_client", ESP_LOG_VERBOSE);
    esp_log_level_set("mqtt_example", ESP_LOG_VERBOSE);
    esp_log_level_set("transport_base", ESP_LOG_VERBOSE);
    esp_log_level_set("esp-tls", ESP_LOG_VERBOSE);
    esp_log_level_set("transport", ESP_LOG_VERBOSE);
    esp_log_level_set("outbox", ESP_LOG_VERBOSE);
    
    esp_mqtt_client_handle_t client = mqtt_app_start();
    xTaskCreate(SendTask, "mqtt_pub_task", 4096, (void*)client, 5, NULL);
    
}

void SendTask(void *pvParameters){
    esp_mqtt_client_handle_t client = (esp_mqtt_client_handle_t)pvParameters;

    int cnt = 0;

    while(1){

        char msg[32];
        sprintf(msg, "Sensor Value : %d", cnt++);

        esp_mqtt_client_publish(client, "dj/sensor", msg, 0, 1, 0);
        
        if (failsafe == 0) vTaskDelay(pdMS_TO_TICKS(2000));
        else if (failsafe == 1) vTaskDelay(pdMS_TO_TICKS(500));
    }
}
void parse_line(char *line){
    if (line[0] != '$') {
        printf("WRONG DATA");
        return;
    }
    char *token = strtok(line, " ");
    token = strtok(NULL, "=");
    token = strtok(NULL, " ");
    float temp = atof(token);
    if (temp>=40) {
        printf("WARNING!!!! TEMP = %.2f\r\n", temp);
        failsafe = 1;
    }
    else {
        printf("TEMP = %.2f\r\n", temp);
        failsafe = 0;
    }



}